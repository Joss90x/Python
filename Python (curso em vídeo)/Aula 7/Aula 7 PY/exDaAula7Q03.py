#Vamos desenvolver um programa que calcula as duas notas de um aluno, e calcule e mostre a média
# Primeiro, vamos pedir as notas do aluno
n1 = float(input("Qual foi a nota da primeira prova? "))
n2 = float(input("E a nota da segunda prova? "))

# Agora, vamos calcular a média
media = (n1 + n2) / 2

# Vamos mostrar o resultado
print(f"A nota final do aluno é: {media:.1f}")

# Explicação:

# Imagina que o n1 é como uma caixinha mágica.
# Nessa caixinha, a gente guarda a primeira nota do aluno.
# É como se fosse um lugar especial só para guardar esse número!

# Agora, o float é como um truque de mágica muito legal!
# Sabe quando você vai comprar um doce e ele custa R$ 2,50?
# O float ajuda o computador a entender esse "2,50".
# Sem ele, o computador ficaria confuso e pensaria que são dois números diferentes!

# Com o float, a gente pode usar números com vírgula, tipo 7,5 ou 8,75.
# Assim, podemos calcular as notas direitinho, sem bagunçar nada!

# É quase como se o float fosse um tradutor que ajuda o computador 
# a entender os números que usamos na vida real. Legal, né?


