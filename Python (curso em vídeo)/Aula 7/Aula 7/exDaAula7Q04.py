#Vamos escrever um programa que leia um valor em metros e o exiba convertido em centímetros e milímetros.
#Ler o valor em metros
metros = float(input('Digite o valor em metros: '))
#Calcular o valor em centímetros
centimetros = metros * 100
#Calcular o valor em milímetros
milimetros = metros * 1000
#Exibir resultados
print(f'O valor em centímetros é {centimetros:.0f} cm e em milímetros é {milimetros:.0f} mm')

# Sim, você obedeceu ao comando corretamente. O programa lê um valor em metros,
# converte para centímetros e milímetros, e exibe os resultados.
